#if !defined(COMPILE_EXCEPTIONAL_CPP_STYLE_CLOSE) || (COMPILE_EXCEPTIONAL_CPP_STYLE_CLOSE & CLOSE_USE_EXCEPTIONAL_CPP_STYLE) != CLOSE_USE_EXCEPTIONAL_CPP_STYLE

#endif  // !defined(COMPILE_EXCEPTIONAL_CPP_STYLE_CLOSE) || (COMPILE_EXCEPTIONAL_CPP_STYLE_CLOSE & CLOSE_USE_EXCEPTIONAL_CPP_STYLE) != CLOSE_USE_EXCEPTIONAL_CPP_STYLE

